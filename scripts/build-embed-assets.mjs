import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const webappDir = path.join(__dirname, "..", "webapp");
const indexPath = path.join(webappDir, "index.html");

let html = fs.readFileSync(indexPath, "utf-8");

function mimeFor(ext) {
  switch (ext.toLowerCase()) {
    case "jpg":
    case "jpeg":
      return "image/jpeg";
    case "png":
      return "image/png";
    case "gif":
      return "image/gif";
    case "svg":
      return "image/svg+xml";
    case "webp":
      return "image/webp";
    default:
      return "application/octet-stream";
  }
}

function toDataUri(filePath) {
  const ext = path.extname(filePath).slice(1);
  const b64 = fs.readFileSync(filePath).toString("base64");
  return `data:${mimeFor(ext)};base64,${b64}`;
}

// 将 CSS 中的本地相对 url() 转为内联 data URI，避免 blob 上下文下相对路径失效
function inlineCssUrls(css, cssFileDir) {
  return css.replace(/url\(\s*(['"]?)([^'")]+)\1\s*\)/g, (m, q, url) => {
    const trimmed = url.trim();
    if (/^(data:|https?:|\/\/|#)/i.test(trimmed)) return m;
    const resolved = path.resolve(cssFileDir, trimmed);
    if (fs.existsSync(resolved)) {
      try {
        return `url(${q}${toDataUri(resolved)}${q})`;
      } catch {
        return m;
      }
    }
    return m;
  });
}

// 1. 内联 <link rel="stylesheet"> → <style>
const linkRegex =
  /<link\s+[^>]*rel=["']stylesheet["'][^>]*href=["']([^"']+)["'][^>]*\/?>/gi;
html = html.replace(linkRegex, (_full, href) => {
  const cleanHref = href.split("?")[0];
  const cssPath = path.resolve(webappDir, cleanHref);
  if (!fs.existsSync(cssPath)) {
    console.warn(`[embed] 缺失 CSS: ${cssPath}`);
    return "";
  }
  let css = fs.readFileSync(cssPath, "utf-8");
  css = inlineCssUrls(css, path.dirname(cssPath));
  return `<style data-src="${cleanHref}">\n${css}\n</style>`;
});

// 2. 内联 JS bundle（来自 scripts/bundle-webapp.mjs 的产物）
const bundlePath = path.join(webappDir, "assets/scripts/bundle.js");
if (!fs.existsSync(bundlePath)) {
  console.error("[embed] 未找到 bundle.js，请先运行 scripts/bundle-webapp.mjs");
  process.exit(1);
}
let bundle = fs.readFileSync(bundlePath, "utf-8");
// 防止 bundle 内的 </script> 提前闭合外层 <script> 标签
bundle = bundle.replace(/<\/script>/gi, "<\\/script>");

// 用单个 bundle <script> 替换所有外部 module 脚本（保留无 src 的内联脚本）
const scriptRegex =
  /<script\s+[^>]*?src=["']([^"']+)["'][^>]*?>\s*<\/script>/gi;
let inserted = false;
html = html.replace(scriptRegex, (_full, src) => {
  if (!/assets\/scripts\//.test(src)) return _full; // 仅处理本地 webapp 脚本
  if (!inserted) {
    inserted = true;
    // bundle.js 是 esbuild IIFE（classic 脚本），必须用 classic <script> 内联，
    // 保持与运行时 AppHost.useBundle() 一致——同步执行、在入口 module 之前把
    // 模块挂载到 window，否则入口 module 拿不到 SectionManager/Todo/store 等全局。
    return `<script>\n${bundle}\n</script>`;
  }
  return "";
});

if (!inserted) {
  console.error("[embed] 未在 index.html 中找到任何 assets/scripts 脚本");
  process.exit(1);
}

// 3. 生成 TS 资源文件（用 JSON.parse 包裹，规避模板字符串中反引号/${ 转义问题）
const outTs = path.join(
  __dirname,
  "..",
  "src",
  "host",
  "webappAssets.generated.ts"
);
const json = JSON.stringify(html);
const ts =
  `// AUTO-GENERATED by scripts/build-embed-assets.mjs — DO NOT EDIT.\n` +
  `// 重新生成: node scripts/build-embed-assets.mjs\n\n` +
  `/**\n` +
  ` * 自包含的 webapp HTML：已内联全部 CSS（含图片 data URI）与 JS bundle。\n` +
  ` * 运行时无需任何外部文件，彻底消除「无法读取 webapp/index.html」类分发问题。\n` +
  ` */\n` +
  `export const WEBAPP_HTML: string = JSON.parse(${json});\n`;

fs.writeFileSync(outTs, ts);
const kb = (Buffer.byteLength(ts, "utf-8") / 1024).toFixed(1);
console.log(`[embed] 生成 ${path.relative(process.cwd(), outTs)} (${kb}KB)`);
